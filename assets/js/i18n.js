(function () {
  'use strict';

  const STORAGE_KEY = 'odh_lang';
  const DEFAULT_LANG = 'en';
  const SUPPORTED = Object.keys(translations);

  // ── Detect language ──────────────────────────────────────
  function detectLang() {
    // 1. URL param: ?lang=de
    const param = new URLSearchParams(window.location.search).get('lang');
    if (param && SUPPORTED.includes(param)) return param;

    // 2. LocalStorage
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && SUPPORTED.includes(stored)) return stored;

    // 3. Browser language
    const browser = (navigator.language || navigator.userLanguage || '').slice(0, 2).toLowerCase();
    if (SUPPORTED.includes(browser)) return browser;

    // 4. Default
    return DEFAULT_LANG;
  }

  // ── Apply translations ───────────────────────────────────
  function applyLang(lang) {
    const dict = translations[lang] || translations[DEFAULT_LANG];

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (dict[key] !== undefined) {
        // Use innerHTML to support <strong> tags in translations
        el.innerHTML = dict[key];
      }
    });

    // Update html lang attribute
    document.documentElement.setAttribute('lang', lang);

    // Update active lang button
    document.querySelectorAll('.lang-switcher button').forEach(btn => {
      btn.classList.toggle('lang-active', btn.textContent.toLowerCase() === lang);
    });

    // Save to storage
    localStorage.setItem(STORAGE_KEY, lang);

    // Update URL param without reload
    const url = new URL(window.location.href);
    url.searchParams.set('lang', lang);
    window.history.replaceState({}, '', url);
  }

  // ── Public setter (called by buttons) ───────────────────
  window.setLang = function (lang) {
    if (SUPPORTED.includes(lang)) applyLang(lang);
  };

  // ── Init ─────────────────────────────────────────────────
  document.addEventListener('DOMContentLoaded', function () {
    applyLang(detectLang());
  });

})();
